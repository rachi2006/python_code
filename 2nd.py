#Basic arithmetic operations.
a = 10
b = 20
print(a+b)
print(a-b)
print(a*b)
print(a%b)
print(a/b)
print(a//b)
print(a**b)

#swapping value of a and b.
a, b = b, a
print("a =", a)
print("b =",  b)

